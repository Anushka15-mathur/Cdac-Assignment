class RectangleArea{
  public static void main(String args[]){
   int length = 5;//predefined length of rectangle
   int breadth = 8;//predefined breadth of rectangle
   int area = length*breadth;//formula of area of the rectangle
   System.out.println("The length of the rectangle is:" + length); 
   System.out.println("The breadth of the rectangle is:" + breadth); 
   System.out.println("The area of the rectangle is:" + area); 
}

}