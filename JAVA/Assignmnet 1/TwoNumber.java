class TwoNumber
{
     public static  void main(String args[])
        {  
          int a = 24;
          double b = 12;

          double sum = a+b;
          System.out.println("Addition = " +sum);

          double division = a/b;
          System.out.println("Division = "+division);

          int result;
          result = (int)division;
          System.out.println("Division after typecasting ="+result);
 
        }

}